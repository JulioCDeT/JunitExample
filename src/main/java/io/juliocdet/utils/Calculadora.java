package io.juliocdet.utils;

public class Calculadora {


    public static int suma (int x, int y) {
        return x + y;
    }

    public static int resta (int x, int y) {
        return x - y;
    }

    public static int division (int x, int y) {
        return x / y;
    }

    public static int multiplicar(int x, int y) {
        return x * y;
    }

}
